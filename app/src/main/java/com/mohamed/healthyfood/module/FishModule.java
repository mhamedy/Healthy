package com.mohamed.healthyfood.module;

public class FishModule {

    private String name_fish, image_fish, info_fish, recipes, texture;

    public FishModule() {}

    public String getName_fish() {
        return name_fish;
    }

    public void setName_fish(String name_fish) {
        this.name_fish = name_fish;
    }

    public String getImage_fish() {
        return image_fish;
    }

    public void setImage_fish(String image_fish) {
        this.image_fish = image_fish;
    }

    public String getInfo_fish() {
        return info_fish;
    }

    public void setInfo_fish(String info_fish) {
        this.info_fish = info_fish;
    }

    public String getRecipes() {
        return recipes;
    }

    public void setRecipes(String recipes) {
        this.recipes = recipes;
    }

    public String getTexture() {
        return texture;
    }

    public void setTexture(String texture) {
        this.texture = texture;
    }
}
