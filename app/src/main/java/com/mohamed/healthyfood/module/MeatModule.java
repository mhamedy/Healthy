package com.mohamed.healthyfood.module;

public class MeatModule {

    private String name_meat, image_meat, info_meat, calories, protein;

    public String getName_meat() {
        return name_meat;
    }

    public void setName_meat(String name_meat) {
        this.name_meat = name_meat;
    }

    public String getImage_meat() {
        return image_meat;
    }

    public void setImage_meat(String image_meat) {
        this.image_meat = image_meat;
    }

    public String getInfo_meat() {
        return info_meat;
    }

    public void setInfo_meat(String info_meat) {
        this.info_meat = info_meat;
    }

    public String getCalories() {
        return calories;
    }

    public void setCalories(String calories) {
        this.calories = calories;
    }

    public String getProtein() {
        return protein;
    }

    public void setProtein(String protein) {
        this.protein = protein;
    }

}
