package com.mohamed.healthyfood.module;

public class MushroomModule {

    private String name_mush, image_mush, info_mush;

    public MushroomModule() {}

    public String getName_mush() {
        return name_mush;
    }

    public void setName_mush(String name_mush) {
        this.name_mush = name_mush;
    }

    public String getImage_mush() {
        return image_mush;
    }

    public void setImage_mush(String image_mush) {
        this.image_mush = image_mush;
    }

    public String getInfo_mush() {
        return info_mush;
    }

    public void setInfo_mush(String info_mush) {
        this.info_mush = info_mush;
    }
}
