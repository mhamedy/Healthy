package com.mohamed.healthyfood.module;

public class NutsModule {

    private String name, image, info, Calories, Carbs, Fat,
            Fiber, Magnesium, Manganese, Protein, Vitamin, info2;


    public NutsModule() {}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getInfo2() {
        return info2;
    }

    public void setInfo2(String info2) {
        this.info2 = info2;
    }

    public String getCalories() {
        return Calories;
    }

    public void setCalories(String calories) {
        Calories = calories;
    }

    public String getCarbs() {
        return Carbs;
    }

    public void setCarbs(String carbs) {
        Carbs = carbs;
    }

    public String getFat() {
        return Fat;
    }

    public void setFat(String fat) {
        Fat = fat;
    }

    public String getFiber() {
        return Fiber;
    }

    public void setFiber(String fiber) {
        Fiber = fiber;
    }

    public String getMagnesium() {
        return Magnesium;
    }

    public void setMagnesium(String magnesium) {
        Magnesium = magnesium;
    }

    public String getManganese() {
        return Manganese;
    }

    public void setManganese(String manganese) {
        Manganese = manganese;
    }

    public String getProtein() {
        return Protein;
    }

    public void setProtein(String protein) {
        Protein = protein;
    }

    public String getVitamin() {
        return Vitamin;
    }

    public void setVitamin(String vitamin) {
        Vitamin = vitamin;
    }
}
